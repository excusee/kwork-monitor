"""
Для каждого кандидата от monitor.py просит Claude (через claude CLI, на Pro-подписке)
оценить релевантность и, если заказ подходит, написать черновик персонализированного
отклика. Не подходящие заказы отбрасываются здесь же (второй, более надёжный фильтр
после простого keyword-отсева в monitor.py).
"""
import json
import subprocess
import sys

BRIEF = """
Контекст обо мне (используй при написании отклика, не упоминай это как "бриф"):
- Я дизайнер карточек маркетплейсов (Wildberries, Ozon, Авито и подобные площадки)
  с собственной стилистикой + сильной экспертизой в нейросетях (AI-генерация
  изображений, промпт-инжиниринг).
- Моё УТП: анализирую конкурентов в нише клиента и пишу более продающие тексты
  для карточек; через нейросети делаю визуалы, которые обычный дизайнер за 500-3000₽
  не сделает или сделает хуже.
- Подписываюсь в конце откликов как "Станислав".
"""

WRITING_RULES = """
Ниже — реальные примеры откликов с биржи фриланса (из методички, проверено
на практике), с разбором, почему они сработали. Пиши отклик В ТОМ ЖЕ ДУХЕ
и СТРУКТУРЕ, что и эти примеры — не копируй текст буквально (тематика других
заказов), а адаптируй ИМЕННО ЭТУ ЛОГИКУ И ФОРМУ под текущий заказ.

ПРИМЕР 1 (взял первый заказ за сутки):
"Александр, здравствуйте! Вот пара моих последних работ в категории Reels
с инфографикой:
- Работа 1
- Работа 2

Так же в моём портфолио есть ролики в Ютуб-формате, так же с инфографикой.

Работаю в Premiere Pro, After Effects, CapCut, готов монтировать Вам видео
на постоянной основе. Если спишемся в течении ближайших часов, то готов
предоставить 1-е видео уже сегодня :)

На Кворке я совсем недавно, однако в индустрии видеомонтажа я более 8 лет
и я был бы рад выполнить Вашу задачу качественно и в кратчайшие сроки.

Надеюсь на Ваш ответ, с уважением, Игорь."

Почему сработало:
1. Сразу показываем, что УЖЕ ДЕЛАЛИ похожую работу (как будет выглядеть
   результат) — самый сильный способ зацепить на старте.
2. О себе — только ПОСЛЕ того, как показали, как поможем клиенту.
3. Короткие абзацы, "красные строки" — комфортно читать.
4. Обращение на "Вы" с большой буквы + лёгкий доброжелательный тон (смайлик).
5. Два триггера на ответ: стимул скорости ("если спишемся в течение часов —
   готов сегодня") и вежливое ожидание ответа в конце.

ПРИМЕР 2 (по словам автора — отклика на него отвечают 90% покупателей):
"Добрый день! Меня заинтересовало ваше предложение. В каких играх нужно
будет делать вырезку с лучшими моментами? Мне очень близка тема игр, слежу
за киберспортом, да и в целом часто поигрываю в них, доказательством может
являться: +-6500 часов в КС ГО, +-1000 часов в Доте и около 1000 часов
в разных играх. Опыт работы в Adobe Premiere и After Effects имеется, могу
делать как трудные, так и легкие работы. Примеры работы именно с играми
у меня отсутствуют, но я могу в качестве испытательного срока сделать один
видеоролик с лучшими моментами матча, либо что-то по вашему желанию. Ко мне
уже обращалось несколько клиентов, они были довольны, думаю, вас тоже могу
порадовать отличными работами за относительно маленький прайс. Цену точную
сказать не могу, т.к. не знаю точные объёмы работ, но уверен, что за один
ролик буду брать 500 рублей. Буду ждать ваш отклик!

О себе: работаю с крупным каналом в Тик Токе. Занимаюсь видеомонтажем
6 лет, сделал очень много видеороликов на Ютубе и других платформах."

Почему сработало (комментарий автора): "Самое главное — задать открытый
вопрос, чтобы на него ответили". Открытый вопрос идёт В САМОМ НАЧАЛЕ,
сразу после приветствия, а не в конце.

КЛЮЧЕВЫЕ ПРИЁМЫ ИЗ ОБОИХ ПРИМЕРОВ (применяй к КОНКРЕТНОМУ заказу):
- Открытый вопрос по сути задачи клиента — самый сильный триггер на ответ,
  может стоять в начале (после короткого приветствия) или в конце.
- Сначала показать, что можешь решить ЭТУ задачу (план/похожий опыт/готовность
  сделать тестовый кусок бесплатно или дёшево, если нет точного примера),
  потом — пара слов "о себе".
- Короткие абзацы с переносами строк.
- "Вы" с большой буквы, вежливый и тёплый, но не подчёркнуто формальный тон.
- Никаких абсолютных обещаний результата ("продажи взлетят в разы") —
  только конкретика по процессу и уверенность в своих силах.
- Не закрывай возражения "методично по пунктам" — это читается как скрипт.
- Подпись в конце — "Станислав" (без фамилии), отдельной строкой.
"""

PROMPT_TEMPLATE = """{brief}

{writing_rules}

Вот заказ с биржи Kwork:
Заголовок: {title}
Бюджет: {budget}
Описание: {description}

Задача:
1. Оцени, действительно ли это заказ на ДИЗАЙН карточек/объявлений товаров или услуг
   для маркетплейсов и досок объявлений (Wildberries, Ozon, Авито и подобные)
   или AI-генерацию изображений/визуалов в этой тематике (не копирайт, не SEO,
   не разработка, не случайное совпадение по ключевым словам).
2. Если НЕ подходит — ответь ровно одним словом: NOT_RELEVANT
3. Если подходит — напиши отклик строго по WRITING_RULES выше (3-5 коротких
   абзацев, на русском, без markdown, чистый текст с переносами строк).

Ответ дай ТОЛЬКО текстом отклика (или ровно NOT_RELEVANT), без пояснений.
"""


def ask_claude(prompt: str) -> str:
    result = subprocess.run(
        ["claude", "-p", prompt],
        capture_output=True,
        text=True,
        timeout=120,
    )
    if result.returncode != 0:
        raise RuntimeError(f"claude CLI failed: {result.stderr}")
    return result.stdout.strip()


def run(candidates: list[dict]) -> list[dict]:
    drafted = []
    for card in candidates:
        prompt = PROMPT_TEMPLATE.format(
            brief=BRIEF,
            writing_rules=WRITING_RULES,
            title=card["title"],
            budget=card["budget"],
            description=card["description"],
        )
        try:
            response = ask_claude(prompt)
        except Exception as e:
            print(f"Ошибка генерации для {card['id']}: {e}", file=sys.stderr)
            continue

        if response.strip() == "NOT_RELEVANT" or "NOT_RELEVANT" in response[:20]:
            continue

        drafted.append({**card, "draft": response})
    return drafted


if __name__ == "__main__":
    candidates = json.load(sys.stdin)
    result = run(candidates)
    json.dump(result, sys.stdout, ensure_ascii=False, indent=2)
