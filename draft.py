"""
Для каждого кандидата от monitor.py просит Claude (через claude CLI, на Pro-подписке)
оценить релевантность и, если заказ подходит, написать черновик персонализированного
отклика. Не подходящие заказы отбрасываются здесь же (второй, более надёжный фильтр
после простого keyword-отсева в monitor.py).
"""
import json
import subprocess
import sys

BRIEF = """
Контекст обо мне (используй при написании отклика, не упоминай это как "бриф"):
- Я дизайнер карточек маркетплейсов (Wildberries, Ozon, Авито и подобные площадки)
  с собственной стилистикой + сильной экспертизой в нейросетях (AI-генерация
  изображений, промпт-инжиниринг).
- Моё УТП: анализирую конкурентов в нише клиента и пишу более продающие тексты
  для карточек; через нейросети делаю визуалы, которые обычный дизайнер за 500-3000₽
  не сделает или сделает хуже.
- Подписываюсь в конце откликов как "Станислав".
"""

WRITING_RULES = """
Правила написания отклика (проверенные приёмы, которые реально поднимают отклик
покупателей на биржах фриланса — не общие советы, а конкретные приёмы):

1. СНАЧАЛА доказательство/план, ПОТОМ "о себе". Открывай отклик не приветствием,
   а сразу конкретным планом под ЭТОТ заказ — что именно сделаешь, как решишь
   задачу. Звучи так, будто уже делал что-то похожее (без прямой лжи про
   конкретные несуществующие проекты — просто уверенно, по существу, без
   оговорок "не уверен, но попробую").
2. Короткие абзацы с переносами строк — не одна сплошная "стена текста".
   План/что сделаю — отдельным абзацем, пара слов о себе — отдельным, концовка —
   отдельной строкой.
3. Тон — искренний и человеческий, не "робот-продажник". Никаких абсолютных
   обещаний результата ("продажи взлетят в разы", "гарантирую кратный рост") —
   это не проверяемо и отталкивает умных клиентов. Вместо этого — конкретика
   по процессу (что и как сделаю) и уверенность в своих силах, без вылизанности
   до отсутствия любых живых деталей.
4. Заканчивай ОДНИМ из двух триггеров на ответ:
   - открытый вопрос клиенту по сути его задачи (заставляет ответить, чтобы
     уточнить), ИЛИ
   - мягкий стимул к быстрому ответу ("если ответите сегодня — могу начать
     сразу" и т.п.)
   Не используй оба варианта одновременно, выбери уместный под конкретный заказ.
5. Не закрывай возражения "методично" по пунктам — это считывается как скрипт
   и отталкивает. Если упоминаешь цену/срок без точных деталей заказа — делай
   это как естественную оговорку, а не как формальный пункт.
6. Подпись в конце — "Станислав" (без фамилии), отдельной строкой.
"""

PROMPT_TEMPLATE = """{brief}

{writing_rules}

Вот заказ с биржи Kwork:
Заголовок: {title}
Бюджет: {budget}
Описание: {description}

Задача:
1. Оцени, действительно ли это заказ на ДИЗАЙН карточек/объявлений товаров или услуг
   для маркетплейсов и досок объявлений (Wildberries, Ozon, Авито и подобные)
   или AI-генерацию изображений/визуалов в этой тематике (не копирайт, не SEO,
   не разработка, не случайное совпадение по ключевым словам).
2. Если НЕ подходит — ответь ровно одним словом: NOT_RELEVANT
3. Если подходит — напиши отклик строго по WRITING_RULES выше (3-5 коротких
   абзацев, на русском, без markdown, чистый текст с переносами строк).

Ответ дай ТОЛЬКО текстом отклика (или ровно NOT_RELEVANT), без пояснений.
"""


def ask_claude(prompt: str) -> str:
    result = subprocess.run(
        ["claude", "-p", prompt],
        capture_output=True,
        text=True,
        timeout=120,
    )
    if result.returncode != 0:
        raise RuntimeError(f"claude CLI failed: {result.stderr}")
    return result.stdout.strip()


def run(candidates: list[dict]) -> list[dict]:
    drafted = []
    for card in candidates:
        prompt = PROMPT_TEMPLATE.format(
            brief=BRIEF,
            writing_rules=WRITING_RULES,
            title=card["title"],
            budget=card["budget"],
            description=card["description"],
        )
        try:
            response = ask_claude(prompt)
        except Exception as e:
            print(f"Ошибка генерации для {card['id']}: {e}", file=sys.stderr)
            continue

        if response.strip() == "NOT_RELEVANT" or "NOT_RELEVANT" in response[:20]:
            continue

        drafted.append({**card, "draft": response})
    return drafted


if __name__ == "__main__":
    candidates = json.load(sys.stdin)
    result = run(candidates)
    json.dump(result, sys.stdout, ensure_ascii=False, indent=2)
